    private void newBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newBtnActionPerformed
        // TODO add your handling code here:
        
        if(!fNameField.getText().equals("")&&!phoneField.getText().equals("")&&!emailField.getText().equals("")){
           emp=new Employee(fNameField.getText(),middleNameField.getText(),lNameField.getText(),emailField.getText(),phoneField.getText());
           try {
               int id=db.insert(emp);
               emp.setId(id);            
               currentEmp=emp;
               db.setIndex(db.getSize()-1);
               setFields();
           } catch (SQLException ex) {
               Logger.getLogger(Gui.class.getName()).log(Level.SEVERE, null, ex);
           }
           clear();
           errorMessage.setText("");
           deleteBtn.setEnabled(true);     
        }
        else if(!isValid(emailField.getText())){
            errorMessage.setText("Email Formate is incorrect");
        }
        else{
           errorMessage.setText("please Fill all the inputs");
        }
        
        
    }//GEN-LAST:event_newBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        
        currentEmp.setfName(fNameField.getText());
        currentEmp.setlName(lNameField.getText());
        currentEmp.setmName(middleNameField.getText());
        currentEmp.setEmail(emailField.getText());
        currentEmp.setPhoneNum(phoneField.getText());
        try {
            db.update(currentEmp);
        } catch (SQLException ex) {
            Logger.getLogger(Gui.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_updateBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
              if(db.getSize()>0){
                  try {
                      db.delete();
                  } catch (SQLException ex) {
                      Logger.getLogger(Gui.class.getName()).log(Level.SEVERE, null, ex);
                  }
              clear(); 
              
              if(db.getSize()==0){
                  deleteBtn.setEnabled(false);
                  
              }
        }
         if(db.getSize()==0){
                  deleteBtn.setEnabled(false);
                  
              }
        
       
        
    }//GEN-LAST:event_deleteBtnActionPerformed

