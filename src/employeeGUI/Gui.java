    private void clear(){
        idField.setText("");
        fNameField.setText("");
        lNameField.setText("");
        middleNameField.setText("");
        phoneField.setText("");
        emailField.setText("");
    }
    public void setFields(){
        idField.setText(Integer.toString(currentEmp.getId()));
        fNameField.setText(currentEmp.getfName());
        lNameField.setText(currentEmp.getlName());
        middleNameField.setText(currentEmp.getmName());
        phoneField.setText(currentEmp.getPhoneNum());
        emailField.setText(currentEmp.getEmail());
    }
    private boolean isValid(String email) {
      String regex = "^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$";
      return email.matches(regex);
   }
    
